#Remember that unless is opposite of if.

=begin

When we talk about if keyword, it is used to make a decision, whether to execute the set of code or not.
But, what if wanted to test the opposite of if? That is, you want to execute the code only when the condition is false.
This is where unless comes into picture.
	
=end

weight = 50
puts "You need to see a doctor" unless weight < 35 || weight>70